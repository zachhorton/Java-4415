public class ArrayEx {
    public static void main(String[] args) {
        Integer[] numbers = {10, 15, 23, 29, 31, 34, 38, 45};
        for (Integer i = 0; i < numbers.length; i++) {
            System.out.print(numbers[i] + ", ");
        }
        System.out.println();
    }
}
